package Jar;



public class Calculator {

	
public  double add(double num1,double num2){

return num1+num2;
}
public  double substract(double num1,double num2){

return num1-num2;
}
public  int multiply(int num1,int num2){

return num1*num2;
}
public  int division(int num1,int num2){

return num1/num2;
}
}

